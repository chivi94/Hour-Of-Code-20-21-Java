public class Variables{

    public static void main(String[] args) {
        //Tipos numericos
        //Enteros
        int numero = 1;
        long numero2 = 2;
        //Coma flotante
        double numero3 = 3.0;
        float numero4 = 4.0f;
        //Caracteres
        char caracterSimple = 'a';
        String cadenaDeCaracteres = "Hola, soy una cadena";

        System.out.println(numero + " " + numero2 + " "+ numero3 + " " + numero4 + " " + caracterSimple + " " + cadenaDeCaracteres);
    }
}
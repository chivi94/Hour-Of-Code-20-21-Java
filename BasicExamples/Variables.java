public class Variables{

    public static void main(String[] args) {
        // Tipos numéricos
        // Enteros
        int numero = 1;
        long numero2 = 2;
        // Coma flotante
        double numero3 = 3.0;
        float numero4 = 4.0f;
        
        // Tipos no numéricos
        // Caracteres
        char caracterSimple = 'a';
        // Cadena de caracteres
        String cadenaDeCaracteres = "Hola, soy una cadena";
        // Lógico (booleanos)
        boolean logico = true;
        
        System.out.println(numero + " " + numero2 + " "+ numero3 + " " + numero4 + " " + caracterSimple + " " + cadenaDeCaracteres + " " + logico);
    }
}